package tree.binarysearchtree.leetcode;

public class TreeNode  {
    int val;
    TreeNode left;
    TreeNode right;
    public TreeNode(int data)
    {
        this.val=val;
    }

    public TreeNode(int data, TreeNode left,TreeNode right)
    {
        this.val=val;
        this.left=left;
        this.right=right;
    }
}
